// 사칙연산 프로그램
// switch - case 사용

let num1 = 1;
let num2 = 5;
let op = "+";

switch (op) {
    case "+":
        console.log(num1 + num2);
        break;
    case "-":
        console.log(num1 - num2);
        break;
    case "*":
        console.log(num1 * num2);
        break;
    case "/":
        console.log(num1 / num2);
        break;
}
